const now = () => (/* @__PURE__ */ new Date()).toISOString().replace("T", " ").substring(0, 16);
function generateBugReportFromTask(task, environment = "", reporter = "", aiAnalysis) {
  const lines = [];
  lines.push(`# Bug Report: ${task.title}`);
  lines.push("");
  lines.push("## Summary");
  lines.push(`> ${task.title}`);
  lines.push("");
  if (task.externalId) {
    lines.push("## Reference");
    lines.push(`- **Issue ID:** ${task.externalId}`);
    if (task.ticketUrl) lines.push(`- **Ticket URL:** ${task.ticketUrl}`);
    lines.push("");
  }
  lines.push("## Environment");
  lines.push(`- **Environment:** ${environment || "Not specified"}`);
  lines.push(`- **Date Reported:** ${now()}`);
  if (reporter) lines.push(`- **Reporter:** ${reporter}`);
  if (task.assignee) lines.push(`- **Assigned To:** ${task.assignee}`);
  lines.push("");
  lines.push("## Priority & Severity");
  lines.push(`- **Priority:** ${task.priority || "Normal"}`);
  lines.push("");
  if (task.description) {
    lines.push("## Description");
    lines.push(task.description);
    lines.push("");
  }
  lines.push("## Steps to Reproduce");
  lines.push("1. ");
  lines.push("2. ");
  lines.push("3. ");
  lines.push("");
  lines.push("## Expected Result");
  lines.push("> _Describe what should happen_");
  lines.push("");
  lines.push("## Actual Result");
  lines.push("> _Describe what actually happens_");
  lines.push("");
  if (task.attachmentUrls?.length) {
    lines.push("## Attachments");
    for (const url of task.attachmentUrls) lines.push(`- ${url}`);
    lines.push("");
  }
  if (task.labels) {
    lines.push("## Labels");
    lines.push(task.labels);
    lines.push("");
  }
  if (aiAnalysis) {
    lines.push("## AI Analysis");
    lines.push(aiAnalysis);
    lines.push("");
  }
  lines.push("---");
  lines.push(`*Generated by QAssistant on ${now()}*`);
  return lines.join("\n");
}
function generateBugReportFromTestCase(tc, testPlanName = "", environment = "", reporter = "", executions, aiAnalysis) {
  const lines = [];
  lines.push(`# Bug Report: ${tc.title}`);
  lines.push("");
  lines.push("## Summary");
  lines.push(`> ${tc.title}`);
  lines.push("");
  lines.push("## Reference");
  if (tc.testCaseId) lines.push(`- **Test Case ID:** ${tc.testCaseId}`);
  if (testPlanName) lines.push(`- **Test Plan:** ${testPlanName}`);
  if (tc.sourceIssueId) lines.push(`- **Source Issue:** ${tc.sourceIssueId}`);
  lines.push("");
  lines.push("## Environment");
  lines.push(`- **Environment:** ${environment || "Not specified"}`);
  lines.push(`- **Date Reported:** ${now()}`);
  if (reporter) lines.push(`- **Reporter:** ${reporter}`);
  lines.push("");
  lines.push("## Priority & Severity");
  lines.push(`- **Priority:** ${tc.priority || "Medium"}`);
  lines.push("");
  if (tc.preConditions) {
    lines.push("## Pre-Conditions");
    lines.push(tc.preConditions);
    lines.push("");
  }
  const steps = tc.steps || tc.testSteps;
  if (steps) {
    lines.push("## Steps to Reproduce");
    lines.push(steps);
    lines.push("");
  }
  if (tc.expectedResult) {
    lines.push("## Expected Result");
    lines.push(tc.expectedResult);
    lines.push("");
  }
  if (tc.actualResult) {
    lines.push("## Actual Result");
    lines.push(tc.actualResult);
    lines.push("");
  }
  if (tc.testData) {
    lines.push("## Test Data");
    lines.push(tc.testData);
    lines.push("");
  }
  if (executions?.length) {
    const recent = [...executions].sort((a, b) => new Date(b.executedAt || 0).getTime() - new Date(a.executedAt || 0).getTime()).slice(0, 5);
    lines.push("## Execution History");
    lines.push("| Execution | Result | Date | Notes |");
    lines.push("|-----------|--------|------|-------|");
    for (const ex of recent) {
      const notes = (ex.notes || "").replace(/\|/g, "\\|");
      const date = ex.executedAt ? ex.executedAt.substring(0, 16).replace("T", " ") : "";
      lines.push(`| ${ex.executionId || ""} | ${ex.result || ""} | ${date} | ${notes} |`);
    }
    lines.push("");
  }
  if (aiAnalysis) {
    lines.push("## AI Analysis");
    lines.push(aiAnalysis);
    lines.push("");
  }
  lines.push("---");
  lines.push(`*Generated by QAssistant on ${now()}*`);
  return lines.join("\n");
}
export {
  generateBugReportFromTask,
  generateBugReportFromTestCase
};
